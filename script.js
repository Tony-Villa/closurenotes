//////////////////// CLOSURES
// A closure is not a feature that we explcitely use. It simply happens automatically in certain situations, we just have to recognize those situations

const secureBooking = function () {
  let passengerCount = 0;

  return function () {
    passengerCount++;
    console.log(`${passengerCount} passengers`);
  };
};

// A closure makes a function remember all the variables that existed at the functions birthplace.

// Even though secureBooking() is off the stack. booker() can still access passengerCount.

// A function has access to the variable environment (VE) of the execution context in which it was created.
// Clodusr: VE attached to the function, exactly as it was at the time and place the function was created
// The scope chain is actually preserved through the closure.
// CLosure has priority over the scope chain

const booker = secureBooking();

booker();
booker();
booker();

// Most formal definition
// A function has access to the variable environment (VE) of the execution context in which it was created.

// Less formal
// A closure gives a function access to all the variables OF ITS PARENT FUNCTION, even AFTER that parent function has returned. The function keeps a reference to its outer scope, which PRESERVES the scope chain throughout time.

// Less formal
// A closure makes sure that a function doesn't lose connection to VARIABLES THAT EXISTED AT THE FUNCTION'S BIRTHPLACE
// (Like a person that doesn't let go of their hometown)

// Less formal
// A closure is like a backpack that a function carries around wherever it goes. This backpack has all the VARIABLES THAT WERE PRESENT IN THE ENVIRONMENT WHERE THE FUNCTION WAS CREATED.
// Whenever the function needs to look up a variable, it can just look into it's backpack

// We do NOT have to create closures, this is a JavaScript feature that happens automatically. We can't even access closed-over variables explicitly. A closure is NOT a tangible JavaScript object.

// We can, however see the closure with:
console.dir(booker);
